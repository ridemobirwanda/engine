async function seedData() {
  console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
  console.log('  MYSQL SAMPLE DATA SEEDER');
  console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

  if (!MYSQL_PASSWORD) {
    console.error('‚ùå Error: MYSQL_PASSWORD environment variable not set!');
    process.exit(1);
  }

  const connection = await mysql.createConnection({
    host: MYSQL_HOST,
    user: MYSQL_USER,
    password: MYSQL_PASSWORD,
    database: MYSQL_DATABASE,
    port: MYSQL_PORT,
  });

  console.log('‚úÖ Connected to MySQL\n');

  try {
    // Clear existing data if --clear flag is present
    if (process.argv.includes('--clear')) {
      console.log('üóëÔ∏è  Clearing existing data...');
      await connection.query('DELETE FROM products');
      await connection.query('DELETE FROM categories');
      console.log('‚úÖ Data cleared\n');
    }

    // Insert categories
    console.log('üì§ Inserting categories...\n');
    let categoryCount = 0;
    for (const category of categories) {
      await connection.query(
        'INSERT INTO categories (name, slug, description, is_active, display_order) VALUES (?, ?, ?, ?, ?)',
        [category.name, category.slug, category.description, category.is_active, category.display_order]
      );
      categoryCount++;
      console.log(`‚úÖ Created: ${category.name}`);
    }

    console.log(`\n‚úÖ Created ${categoryCount} categories\n`);

    // Insert products
    console.log('üì§ Inserting products...\n');
    let productCount = 0;
    for (const product of products) {
      await connection.query(
        `INSERT INTO products (
          name, short_description, description, price, compare_price,
          brand, model, engine_type, displacement, fuel_type,
          \`condition\`, stock_quantity, is_active, is_featured,
          images, category_id
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [
          product.name,
          product.short_description,
          product.description,
          product.price,
          product.compare_price || null,
          product.brand,
          product.model,
          product.engine_type,
          product.displacement,
          product.fuel_type,
          product.condition,
          product.stock_quantity,
          product.is_active,
          product.is_featured,
          product.images,
          product.category_id,
        ]
      );
      productCount++;
      console.log(`‚úÖ Created: ${product.name}`);
    }

    console.log(`\nüìä Summary:`);
    console.log(`   ‚úÖ Categories: ${categoryCount}`);
    console.log(`   ‚úÖ Products: ${productCount}`);
    console.log(`\nüéâ Sample data seeded successfully!`);
    console.log(`\nüí° Visit http://localhost:21201/admin/products to see your products!\n`);

  } catch (error) {
    console.error('‚ùå Error seeding data:', error);
  } finally {
    await connection.end();
    console.log('üëã MySQL connection closed\n');
  }
}

seedData();

